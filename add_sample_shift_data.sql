SELECT 
    s.date,
    e.first_name,
    e.last_name,
    st.name as shift_type,
    s.start_time,
    s.end_time,
    s.status
FROM shifts s
JOIN employees e ON s.employee_id = e.id
JOIN shift_types st ON s.shift_type_id = st.id
WHERE s.date BETWEEN '2025-09-01' AND '2025-09-30'
ORDER BY s.date, s.start_time
LIMIT 10;
